    public void fileDetails(String Id) {
        ResultSet rs;
        String query = "SELECT *\n"
                + "FROM apartment\n"
                + "INNER JOIN quality_level ON apartment.quality_id=quality_level.id\n"
                + "INNER JOIN apartment_type ON apartment.type_id=apartment_type.id\n"
                + "INNER JOIN `condition` ON apartment.condition_id=`condition`.id\n"
                + "INNER JOIN apartment_status ON apartment.apartment_status_id=apartment_status.id\n"
                + "WHERE apartment.id = '" + Id + "'";
        try {
            rs = MySQL.search(query);
            if (rs.next()) {
                if (rs.getString("apartment_status.name").equals("Reserved") || rs.getString("apartment_status.name").equals("Sold")) {
                    JOptionPane.showMessageDialog(this, "Update option not available for Reserved or Sold Apartments", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    jLabel23.setText(Id);
                    jLabel24.setText(rs.getString("floor"));
                    jLabel26.setText(rs.getString("apartment.num_room"));
                    jLabel22.setText(rs.getString("apartment.num_bathroom"));
                    jTextArea1.setText(rs.getString("apartment.description"));
                    jLabel27.setText(rs.getString("quality_level.name"));
                    Double qulityRate = Double.parseDouble(rs.getString("quality_level.addion_rate"));
                    jLabel30.setText(df.format(qulityRate));
                    jTextArea2.setText(rs.getString("quality_level.description"));
                    jLabel28.setText(rs.getString("apartment_type.name"));
                    Double facilityRate = Double.parseDouble(rs.getString("apartment_type.addition_rate"));
                    jLabel31.setText(df.format(facilityRate));
                    jLabel29.setText(rs.getString("condition.name"));
                    Double conditionRate = Double.parseDouble(rs.getString("condition.addion_rate"));
                    jLabel32.setText(df.format(conditionRate));
                    Double rate = Double.parseDouble(rs.getString("apartment.rent_rate"));
                    Double total = rate + qulityRate + conditionRate + facilityRate;
                    jLabel33.setText(df.format(rate));
                    jLabel34.setText(df.format(Double.parseDouble(rs.getString("apartment.advance_rate"))));
                    jLabel35.setText(df.format(total));
                }

            } else {
                JOptionPane.showMessageDialog(this, "Record not found. Please try agin later", "Warning", JOptionPane.WARNING_MESSAGE);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

